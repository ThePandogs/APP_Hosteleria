package modelo;

import java.util.ArrayList;
import java.util.List;

/**
 *
 * @author ThePandogs
 */
public class Sala {

    int id;
    String nombre;

    List<Mesa> mesas;

    public Sala(int id, String nombre) {
        this.id = id;
        this.nombre = nombre;
        mesas = new ArrayList();

    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public List<Mesa> getMesas() {
        return mesas;
    }

    public void setMesas(List<Mesa> mesas) {
        this.mesas = mesas;
    }

    
}
